# calculator in python(v0.3)


def add(num1, num2):
     return num1+num2

def mul(num1, num2):
     return num1*num2

def sub(num1, num2):
     return num1-num2

def div(num1, num2):
     return num1/num2
    

def square(num1):
     return num1 ** num1


result  = add(5,3)
print(result)
print(mul(5,3))
print(sub(5,3))
print(div(6, 3))
print(square(5))
